def _build_hex_index():
    """构建卦名索引（全名 + 单字别名均可查）。"""
    idx = {}
    for name, alias, upper, lower, palace, palace_wx, world in _HEX:
        rec = {
            "name": name, "alias": alias, "upper": upper, "lower": lower,
            "palace": palace, "palace_wx": palace_wx, "world": world,
        }
        idx[name] = rec
        idx[alias] = rec
    return idx


HEXAGRAMS = _build_hex_index()


def find_hexagram(key):
    """按全名、单字别名或'上下卦'查找卦。返回卦记录 dict 或 None。"""
    if key in HEXAGRAMS:
        return HEXAGRAMS[key]
    # 去空格再试
    k = key.replace(" ", "").strip()
    if k in HEXAGRAMS:
        return HEXAGRAMS[k]
    return None
